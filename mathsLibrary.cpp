#include<bits/stdc++.h>
#include<cmath>
using namespace std;
int main()
{
    int x=4,y=6;
    cout<<"The greatest number="<<fmax(x,y)<<endl;
    cout<<"The smaller number="<<fmin(x,y)<<endl;
    cout<<"The difference of two number="<<fdim(y,x)<<endl;
    cout<<"2 to the power 9="<<pow(2,9)<<endl;
    cout<<"square root of the number="<<sqrt(169)<<endl;
    cout<<"round figure of the number="<<round(4.6)<<endl;
    cout<<"round figure of the number="<<round(2.3)<<endl;
    cout<<"terminate digit after the decimal="<<floor(6.056)<<endl;
    cout<<"The logrithm of the number="<<log(10);

}